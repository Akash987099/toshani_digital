<script>
      document.addEventListener("DOMContentLoaded", () => {
        const counters = [
          { id: "counter-happy-customers", value: 165489 },
          { id: "counter-employees", value: 205 },
          { id: "counter-kiosk", value: 655 },
          { id: "counter-digital-centers", value: 23000 },
        ];

        const duration = 3000;

        counters.forEach((counter) => {
          const element = document.getElementById(counter.id);
          let start = 0;
          const increment = Math.ceil(counter.value / (duration / 50));

          const updateCounter = () => {
            start += increment;
            if (start > counter.value) {
              start = counter.value;
            }
            element.textContent = start.toLocaleString();
            if (start < counter.value) {
              requestAnimationFrame(updateCounter);
            }
          };

          updateCounter();
        });
      });
    </script>


